module Print : sig
  val exp_to_string : exp -> string
  val typ_to_string : typ -> string
end = struct
  let rec separate separator f l =
    match l with
    | []       -> ""
    | [x]      -> f x
    | x1 :: xs -> f x1 ^ separator ^ separate separator f xs

  let paren lvl oplvl string =
    if oplvl < lvl then
      "(" ^ string ^ ")"
    else
      string

  let rec assoc_opt l r =
    match l with
    | [] -> None
    | (a, r') :: l' -> if r == r' then Some a else assoc_opt l' r

  let po_prec p =
    match p with
    | Or           -> 1
    | And          -> 2
    | Equals       -> 3
    | NotEquals    -> 3
    | LessThan     -> 3
    | GreaterThan  -> 3
    | LessEqual    -> 3
    | GreaterEqual -> 3
    | Plus         -> 4
    | Minus        -> 4
    | Times        -> 5
    | Div          -> 5
    | Negate       -> 7

  let po_to_str p =
    match p with
    | Or           -> "||"
    | And          -> "&&"
    | Equals       -> "="
    | NotEquals    -> "!="
    | LessThan     -> "<"
    | GreaterThan  -> ">"
    | LessEqual    -> "<="
    | GreaterEqual -> ">="
    | Plus         -> "+"
    | Minus        -> "-"
    | Times        -> "*"
    | Div          -> "/"
    | Negate       -> "~"

  let typ_to_string ty =
    let counter = ref 0 in
    let freshVar () =
      counter := !counter + 1;
      "a" ^ (string_of_int !counter)
    in
    let rec physical_assoc_opt l r =
      match l with
      | [] -> None
      | (a, r') :: l' ->
         (*
          * This comparision should be physical comparision
          * to distinguish two empty type variables.
          *)
         if r == r' then Some a else physical_assoc_opt l' r
    in
    let rec typstr lvl l ty =
      match ty with
      | TArrow (domain, range) ->
         let (l', t) = typstr 1 l domain in
         let (l'', t') = typstr 0 l' range in
         (l'', paren lvl 0 (t ^ " -> " ^ t'))
      | TProduct [] -> (l, "()")
      | TProduct [x] -> typstr lvl l x
      | TProduct (t :: ts) ->
         let f = fun (l, t) p ->
           let (l', t') = typstr 2 l p in
           (l', t ^ " * " ^ t')
         in
         let (l', t') = List.fold_left f (typstr 2 l t) ts in
         (l', paren lvl 1 t')
      | TInt -> (l, "int")
      | TBool -> (l, "bool")
      | TVar x ->
         match !x with
         | None ->
            begin match physical_assoc_opt l x with
            | None -> let a = freshVar () in ((a, x) :: l, a)
            | Some a -> (l, a)
            end
         | Some t -> typstr 1 l t
    in
    let (_, s) = typstr 0 [] ty in
    s

  let rec expstr lvl e =
    match e with
    | Int i -> string_of_int i
    | Bool b -> string_of_bool b
    | If (ec, et, ef) ->
       paren lvl 0 ("if " ^ expstr 0 ec ^ " then " ^ expstr 0 et ^ " else " ^ expstr 0 ef)
    | Primop (p, []) -> "(bad primop)"
    | Primop (p, [e]) -> paren lvl (po_prec p) (po_to_str p ^ expstr (po_prec p + 1) e)
    | Primop (p, e :: es) ->
       (* All prim ops are assumed to be left associative *)
       let expstr' b a = b ^ " " ^ po_to_str p ^ " " ^ (expstr (po_prec p + 1) a) in
       paren lvl (po_prec p) (List.fold_left expstr' (expstr (po_prec p) e) es)
    | Tuple es -> "(" ^ separate ", " (expstr 0) es ^ ")"
    | Fn (x, t, e) ->
       begin match t with
       | None -> paren lvl 0 ("fn " ^ x ^  " => " ^ expstr 0 e)
       | Some t -> paren lvl 0 ("fn " ^ x ^ ": " ^ typ_to_string t ^ " => " ^ expstr 0 e)
       end
    | Rec (ff, ftype, e) ->
       paren lvl 0 ("rec " ^ ff ^  " : " ^ typ_to_string ftype ^ " => " ^ expstr 0 e)
    | Let (decs, e) ->
       "let " ^ separate "\n    " decstr decs ^ " in " ^ expstr 0 e ^ " end"
    | Apply (e1, e2) ->
       paren lvl 6 ((expstr 6 e1) ^ " " ^ (expstr 7 e2))
    | Var v -> v
    | Anno (e, t) -> paren lvl 0 (expstr 1 e ^ " : " ^ typ_to_string t)

  and decstr e =
    match e with
    | Val (Rec (ff, ftype, Fn (x, _, body)), gg)
         when ff = gg ->
       "fun " ^ ff ^ " : " ^ typ_to_string ftype ^ " " ^ x ^  " = " ^ expstr 0 body
    | Val (e1, x) ->
       "val " ^ x ^ " = " ^ expstr 0 e1
    | ByName (e1, x) ->
       "name " ^ x ^ " = " ^ expstr 0 e1
    | Valtuple (e1, xs) ->
       "val (" ^ separate ", " (fun name -> name) xs ^ ") = " ^ expstr 0 e1

  let exp_to_string e = expstr 0 e

  (* Do we need to export these AST printers too? *)
  let rec ast_to_string e =
    match e with
    | Int i -> "Int (" ^ string_of_int i ^ ")"
    | Bool b -> "Bool " ^ string_of_bool b
    | If (ec, et, ef) ->
       "If (" ^ ast_to_string ec ^  ", " ^ ast_to_string et ^ ", " ^ ast_to_string ef ^ ")"
    | Primop (p, es) -> "Primop (" ^ po_to_str p ^ ", [" ^ separate ", " ast_to_string es ^ "])"
    | Tuple es -> "Tuple (" ^ separate ", " ast_to_string es ^ ")"
    | Fn (x, t, e) ->
       begin match t with
       | None -> "Fn (" ^ x ^  ", None, " ^ ast_to_string e ^ ")"
       | Some t -> "Fn (" ^ x ^  ", Some (" ^ ast_typ_to_string t ^ "), " ^ ast_to_string e ^ ")"
       end
    | Rec (ff, ftype, e) ->
       "Rec (" ^ ff ^  ", " ^ ast_typ_to_string ftype ^ ", " ^ ast_to_string e ^ ")"
    | Let (decs, e) ->
       "Let ([" ^ separate "; " ast_decstr decs ^ "], " ^ ast_to_string e ^ ")"
    | Apply (e1, e2) ->
       "Apply (" ^ ast_to_string e1 ^ ", " ^ ast_to_string e2 ^ ")"
    | Var v -> "Var " ^ v
    | Anno (e, t) -> "Anno (" ^ ast_to_string e ^ ", " ^ ast_typ_to_string t ^ ")"

  and ast_decstr e =
    match e with
    | Val (e1, x) ->
       "Val (" ^ ast_to_string e1 ^ ", " ^ x ^ ")"
    | ByName (e1, x) ->
       "ByName (" ^ ast_to_string e1 ^ ", " ^ x ^ ")"
    | Valtuple (e1, xs) ->
       "ValTuple (" ^ ast_to_string e1 ^ ", [" ^ separate "; " (fun name -> name) xs ^ "])"

  and ast_typ_to_string e =
    match e with
    | TArrow (t1, t2) ->
       "TArrow (" ^ ast_typ_to_string t1 ^ ", " ^ ast_typ_to_string t2 ^ ")"
    | TProduct xs -> "TProduct [" ^ separate "; " ast_typ_to_string xs ^ "]"
    | TInt -> "TInt"
    | TBool -> "TBool"
    | TVar x ->
       match !x with
       | None -> "TVar None"
       | Some t -> "TVar (Some " ^ ast_typ_to_string t ^ ")"
end
